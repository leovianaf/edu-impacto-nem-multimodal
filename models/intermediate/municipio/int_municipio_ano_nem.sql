select
    ano,
    id_municipio,
    min(sg_uf) as sg_uf,
    min(no_uf) as no_uf,
    min(no_municipio) as no_municipio,
    min(periodo_nem) as periodo_nem,
    count(distinct case when coalesce(in_med_compat, 0) = 1 then id_escola end) as qt_escolas_em,
    count(distinct case when coalesce(in_em_tempo_integral_compat, 0) = 1 then id_escola end) as qt_escolas_em_tempo_integral,
    count(distinct case when coalesce(in_itinerario_aprofundamento, 0) = 1 then id_escola end) as qt_escolas_itinerario_aprofundamento,
    count(distinct case when coalesce(in_itinerario_tecn_prof, 0) = 1 then id_escola end) as qt_escolas_itinerario_tecn_prof,
    sum(coalesce(qt_mat_med_compat, 0)) as qt_mat_med,
    sum(coalesce(qt_mat_med_prop, 0)) as qt_mat_med_prop,
    sum(coalesce(qt_mat_med_nm, 0)) as qt_mat_med_nm,
    sum(coalesce(qt_mat_med_int_compat, 0)) as qt_mat_med_int,
    sum(coalesce(qt_mat_med_ifa, 0)) as qt_mat_med_ifa,
    sum(coalesce(qt_mat_med_ifa_ling, 0)) as qt_mat_med_ifa_ling,
    sum(coalesce(qt_mat_med_ifa_mate, 0)) as qt_mat_med_ifa_mate,
    sum(coalesce(qt_mat_med_ifa_cienc, 0)) as qt_mat_med_ifa_cienc,
    sum(coalesce(qt_mat_med_ifa_huma, 0)) as qt_mat_med_ifa_huma,
    sum(coalesce(qt_mat_med_iftp_ct, 0)) as qt_mat_med_iftp_ct,
    sum(coalesce(qt_mat_med_iftp_qp, 0)) as qt_mat_med_iftp_qp,
    sum(coalesce(qt_mat_med_arti_iftp_ct, 0)) as qt_mat_med_arti_iftp_ct,
    sum(coalesce(qt_mat_med_arti_iftp_qp, 0)) as qt_mat_med_arti_iftp_qp,
    sum(coalesce(qt_tur_med_compat, 0)) as qt_tur_med,
    sum(coalesce(qt_tur_med_prop, 0)) as qt_tur_med_prop,
    sum(coalesce(qt_tur_med_nm, 0)) as qt_tur_med_nm,
    sum(coalesce(qt_tur_med_int_compat, 0)) as qt_tur_med_int,
    sum(coalesce(qt_tur_med_ead, 0)) as qt_tur_med_ead,
    sum(coalesce(qt_tur_med_ifa, 0)) as qt_tur_med_ifa,
    sum(coalesce(qt_tur_med_iftp_ct, 0)) as qt_tur_med_iftp_ct,
    sum(coalesce(qt_tur_med_iftp_qp, 0)) as qt_tur_med_iftp_qp,
    sum(coalesce(qt_doc_med_compat, 0)) as qt_doc_med,
    sum(coalesce(qt_doc_med_prop, 0)) as qt_doc_med_prop,
    sum(coalesce(qt_doc_med_nm, 0)) as qt_doc_med_nm,
    sum(coalesce(qt_doc_med_iftp_ct, 0)) as qt_doc_med_iftp_ct,
    sum(coalesce(qt_doc_med_iftp_qp, 0)) as qt_doc_med_iftp_qp,
    sum(coalesce(qt_doc_bas_disc_projeto_de_vida, 0)) as qt_doc_projeto_vida,
    sum(coalesce(qt_doc_bas_disc_profissiona, 0)) as qt_doc_componente_profissionalizante,
    sum(coalesce(qt_doc_bas_disc_info_computacao, 0)) as qt_doc_info_computacao,
    sum(coalesce(qt_gest_bas_espec_ens_medio, 0)) as qt_gest_espec_ens_medio,
    case when sum(coalesce(qt_mat_med_compat, 0)) > 0 then sum(coalesce(qt_mat_med_int_compat, 0)) * 1.0 / sum(coalesce(qt_mat_med_compat, 0)) else null end as prop_mat_em_integral,
    case when sum(coalesce(qt_mat_med_compat, 0)) > 0 then (sum(coalesce(qt_mat_med_iftp_ct, 0)) + sum(coalesce(qt_mat_med_iftp_qp, 0)) + sum(coalesce(qt_mat_med_arti_iftp_ct, 0)) + sum(coalesce(qt_mat_med_arti_iftp_qp, 0))) * 1.0 / sum(coalesce(qt_mat_med_compat, 0)) else null end as prop_mat_em_tecnico_profissional,
    case when count(distinct case when coalesce(in_med_compat, 0) = 1 then id_escola end) > 0 then count(distinct case when coalesce(in_em_tempo_integral_compat, 0) = 1 then id_escola end) * 1.0 / count(distinct case when coalesce(in_med_compat, 0) = 1 then id_escola end) else null end as prop_escolas_em_integral,
    case when count(distinct case when coalesce(in_med_compat, 0) = 1 then id_escola end) > 0 then count(distinct case when coalesce(in_itinerario_tecn_prof, 0) = 1 then id_escola end) * 1.0 / count(distinct case when coalesce(in_med_compat, 0) = 1 then id_escola end) else null end as prop_escolas_itinerario_tecn_prof
from {{ ref('int_censo_escola_ano_2019_2025') }}
group by ano, id_municipio
