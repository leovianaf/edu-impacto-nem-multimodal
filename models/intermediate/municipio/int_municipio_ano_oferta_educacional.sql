select
    ano,
    id_municipio,
    min(sg_uf) as sg_uf,
    min(no_uf) as no_uf,
    min(no_municipio) as no_municipio,
    min(periodo_nem) as periodo_nem,
    count(distinct id_escola) as qt_escolas,
    sum(case when tp_localizacao = 1 then 1 else 0 end) as qt_escolas_urbanas,
    sum(case when tp_localizacao = 2 then 1 else 0 end) as qt_escolas_rurais,
    sum(case when tp_dependencia = 1 then 1 else 0 end) as qt_escolas_federais,
    sum(case when tp_dependencia = 2 then 1 else 0 end) as qt_escolas_estaduais,
    sum(case when tp_dependencia = 3 then 1 else 0 end) as qt_escolas_municipais,
    sum(case when tp_dependencia = 4 then 1 else 0 end) as qt_escolas_privadas,
    sum(case when coalesce(in_bas, 0) = 1 then 1 else 0 end) as qt_escolas_eb,
    sum(case when coalesce(in_med_compat, 0) = 1 then 1 else 0 end) as qt_escolas_em,
    sum(case when coalesce(in_prof_tec_compat, 0) = 1 then 1 else 0 end) as qt_escolas_prof_tec,
    sum(case when coalesce(in_eja, 0) = 1 then 1 else 0 end) as qt_escolas_eja,
    sum(case when coalesce(in_em_tempo_integral_compat, 0) = 1 then 1 else 0 end) as qt_escolas_em_tempo_integral,
    sum(case when coalesce(in_biblioteca, 0) = 1 then 1 else 0 end) as qt_escolas_biblioteca,
    sum(case when coalesce(in_internet, 0) = 1 then 1 else 0 end) as qt_escolas_internet,
    sum(case when coalesce(in_internet_aprendizagem, 0) = 1 then 1 else 0 end) as qt_escolas_internet_aprendizagem,
    sum(case when coalesce(in_laboratorio_ciencias, 0) = 1 then 1 else 0 end) as qt_escolas_lab_ciencias,
    sum(case when coalesce(in_laboratorio_informatica, 0) = 1 then 1 else 0 end) as qt_escolas_lab_info,
    sum(case when coalesce(in_laboratorio_educ_prof, 0) = 1 then 1 else 0 end) as qt_escolas_lab_educ_prof,
    sum(case when coalesce(in_quadra_esportes, 0) = 1 then 1 else 0 end) as qt_escolas_quadra,
    sum(coalesce(qt_salas_utilizadas, 0)) as qt_salas_utilizadas,
    sum(coalesce(qt_mat_bas, 0)) as qt_mat_bas,
    sum(coalesce(qt_mat_inf, 0)) as qt_mat_inf,
    sum(coalesce(qt_mat_fund, 0)) as qt_mat_fund,
    sum(coalesce(qt_mat_med_compat, 0)) as qt_mat_med,
    sum(coalesce(qt_mat_med_int_compat, 0)) as qt_mat_med_int,
    sum(coalesce(qt_mat_prof, 0)) as qt_mat_prof,
    sum(coalesce(qt_mat_prof_tec_compat, 0)) as qt_mat_prof_tec,
    sum(coalesce(qt_doc_bas, 0)) as qt_doc_bas,
    sum(coalesce(qt_doc_fund, 0)) as qt_doc_fund,
    sum(coalesce(qt_doc_med_compat, 0)) as qt_doc_med,
    sum(coalesce(qt_doc_prof_tec_compat, 0)) as qt_doc_prof_tec,
    sum(coalesce(qt_tur_bas, 0)) as qt_tur_bas,
    sum(coalesce(qt_tur_fund, 0)) as qt_tur_fund,
    sum(coalesce(qt_tur_med_compat, 0)) as qt_tur_med,
    sum(coalesce(qt_tur_med_int_compat, 0)) as qt_tur_med_int,
    sum(coalesce(qt_tur_prof_tec_compat, 0)) as qt_tur_prof_tec,
    sum(coalesce(qt_gest_bas, 0)) as qt_gest_bas,
    case when count(distinct id_escola) > 0 then sum(case when coalesce(in_med_compat, 0) = 1 then 1 else 0 end) * 1.0 / count(distinct id_escola) else null end as prop_escolas_em,
    case when count(distinct id_escola) > 0 then sum(case when coalesce(in_prof_tec_compat, 0) = 1 then 1 else 0 end) * 1.0 / count(distinct id_escola) else null end as prop_escolas_prof_tec,
    case when count(distinct id_escola) > 0 then sum(case when coalesce(in_internet, 0) = 1 then 1 else 0 end) * 1.0 / count(distinct id_escola) else null end as prop_escolas_internet,
    case when count(distinct id_escola) > 0 then sum(case when coalesce(in_internet_aprendizagem, 0) = 1 then 1 else 0 end) * 1.0 / count(distinct id_escola) else null end as prop_escolas_internet_aprendizagem,
    case when count(distinct id_escola) > 0 then sum(case when coalesce(in_biblioteca, 0) = 1 then 1 else 0 end) * 1.0 / count(distinct id_escola) else null end as prop_escolas_biblioteca,
    case when count(distinct id_escola) > 0 then sum(case when coalesce(in_laboratorio_informatica, 0) = 1 then 1 else 0 end) * 1.0 / count(distinct id_escola) else null end as prop_escolas_lab_info,
    case when count(distinct id_escola) > 0 then sum(case when coalesce(in_laboratorio_ciencias, 0) = 1 then 1 else 0 end) * 1.0 / count(distinct id_escola) else null end as prop_escolas_lab_ciencias,
    case when count(distinct id_escola) > 0 then sum(case when coalesce(in_quadra_esportes, 0) = 1 then 1 else 0 end) * 1.0 / count(distinct id_escola) else null end as prop_escolas_quadra,
    case when sum(coalesce(qt_tur_med_compat, 0)) > 0 then sum(coalesce(qt_mat_med_compat, 0)) * 1.0 / sum(coalesce(qt_tur_med_compat, 0)) else null end as media_matriculas_por_turma_em,
    case when sum(coalesce(qt_doc_med_compat, 0)) > 0 then sum(coalesce(qt_mat_med_compat, 0)) * 1.0 / sum(coalesce(qt_doc_med_compat, 0)) else null end as media_matriculas_por_docente_em,
    case when sum(coalesce(qt_tur_prof_tec_compat, 0)) > 0 then sum(coalesce(qt_mat_prof_tec_compat, 0)) * 1.0 / sum(coalesce(qt_tur_prof_tec_compat, 0)) else null end as media_matriculas_por_turma_prof_tec
from {{ ref('int_censo_escola_ano_2019_2025') }}
group by ano, id_municipio
