    case when coalesce(escola.qt_mat_med_compat, 0) > 0 then escola.qt_mat_med_int_compat * 1.0 / escola.qt_mat_med_compat else null end as prop_mat_em_integral_escola,
    case when coalesce(escola.qt_mat_med_compat, 0) > 0 then escola.qt_mat_prof_tec_compat * 1.0 / escola.qt_mat_med_compat else null end as prop_mat_em_tecnico_profissional_escola,
